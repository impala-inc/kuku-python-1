# 「1-3」の「who」や「where」はデータを識別するために名前をつけるておくことができる「変数」というものです
# 指示された文章を表示するようにコードを追加してください

who = '私の'
what = '好きな食べ物は'
food = 'オムライス'

print(who + what + food + 'です')

# 「田中さんの好きな食べ物はオムライスです」と表示させてください
who = '田中さんの'
print(who + what + food + 'です')  # この行は変更しない

# 「田中さんの好きな食べ物はピーマンです」と表示させてください
food = 'ピーマン'
print(who + what + food + 'です')  # この行は変更しない

# 「田中さんの嫌いな食べ物はピーマンです」と表示させてください
what = '嫌いな食べ物は'
print(who + what + food + 'です')  # この行は変更しない
